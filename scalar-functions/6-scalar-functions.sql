SELECT CURDATE(); -- Returns the current date, e.g. '2023-03-08'
SELECT CURTIME(); -- Returns the current time, e.g. '14:32:15'
SELECT DATE('2023-03-08 14:32:15'); -- Returns '2023-03-08'
SELECT DAY('2023-03-08 14:32:15'); -- Returns 8
SELECT HOUR('2023-03-08 14:32:15'); -- Returns 14
SELECT MINUTE('2023-03-08 14:32:15'); -- Returns 32
SELECT NOW(); -- Returns the current datetime, e.g. '2023-03-08 14:32:15'
